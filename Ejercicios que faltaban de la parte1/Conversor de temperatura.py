# Mensaje de bienvenida para el usuario
print("Bienvenido al conversor de temperatura")
print("Convierte grados Celsius a Fahrenheit.")

C = float(input("\nPor favor, ingresa la temperatura en Celsius: "))

F = (C * 9 / 5) + 32

print(f"Has ingresado {C} C.")
print(f"el valor equivalente es de {F} F.")

print("\nGracias por usar el conversor.")
